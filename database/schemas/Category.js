import {mongoose, Mongoose} from 'mongoose';

const Category = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    unique: true,
  },
  description: {
    type: String,
    required: true,
  },
  type: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now(),
  },
});

export default Mongoose.models?.Category ||
  mongoose.model('Category', Category);
